import streamlit as st

st.title('Datathon')

st.markdown("""
### Objetivo

O grande objetivo do Datathon é que você, como cientista de dados, crie uma proposta preditiva ou, como analista de dados, realize uma proposta analítica para demonstrar o impacto que a ONG “Passos Mágicos” tem realizado sobre a comunidade que atende.

A associação busca instrumentalizar o uso da educação como ferramenta para a mudança das condições de vida das crianças e jovens em vulnerabilidade social. Com base no dataset de pesquisa extensiva do desenvolvimento educacional no período de 2020, 2021 e 2022, você pode entregar uma das seguintes propostas:

- **Proposta analítica**: Demonstrar os impactos que a ONG “Passos Mágicos” realizou sobre o desempenho de estudantes e levantar indicadores de performance. Para isso, deve-se criar um **dashboard** e um **storytelling** que contem uma história com os dados para auxiliar a Passos Mágicos a tomar as melhores decisões com base nos indicadores e conhecer o perfil dos estudantes.

- **Proposta preditiva**: Criar um **modelo preditivo** para prever o comportamento do estudante com base em algumas variáveis cruciais para a identificação de seu desenvolvimento. Na proposta preditiva, você pode utilizar a criatividade para propor uma solução de algoritmo supervisionado ou não supervisionado. A ideia é aplicar técnicas como **Machine Learning, Deep Learning ou Processamento de Linguagem Natural**.

### Base de Dados

A base de dados contém informações educacionais e socioeconômicas dos estudantes da Passos Mágicos. Foram disponibilizadas **duas bases de dados** com as características de desenvolvimento educacional e questões socioeconômicas dos estudantes, além de um **dicionário de dados** com o mapeamento de todas as variáveis. Além disso, alguns **relatórios de pesquisa** realizados pela Passos Mágicos também estão disponíveis para auxiliar no conhecimento do negócio.

### Entrega

Os participantes podem optar por entregar **apenas uma das propostas ou ambas**.

- **Proposta analítica**: A entrega deve conter um **dashboard** e um **relatório** com a análise dos dados.
- **Proposta preditiva**: A entrega deve conter um **modelo preditivo** com o **deploy realizado no Streamlit**.

""")